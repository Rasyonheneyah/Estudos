# Insira valores, vejamos qual é o maior, e qual é o menor.
Valor=int(input('Insira um valor:'))
Maior=Valor
Menor=Valor
while Valor!=0:
    if Valor>Maior:
        Maior=Valor
    else:
        Valor<Menor
        Menor=Valor
    Valor=int(input('Insira um valor:'))
print('O maior número é ', Maior, ', já o menor e', Menor)
